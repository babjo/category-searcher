	public String parseNext(String str) {
		int startSubLength = str.length();

		String[] split = { " ", "]", ")", "/" };

		for (int i = 0; i < split.length; i++) {
			startSubLength = getIndexOf(str, startSubLength, split[i]);
		}

		return str.substring(startSubLength, str.length());
	}

	public int getIndexOf(String str, int startSubLength, String splitIndicator) {
//		System.out.println(str+":"+str.indexOf(splitIndicator));
		if (str.indexOf(splitIndicator) >= 0 && startSubLength >= str.indexOf(splitIndicator)) {
			startSubLength = str.indexOf(splitIndicator)+1;
		}
		return startSubLength;
	}

